                   ply:Kick("You have been warned many times about placing these props, yet you still continued to place them!")

                   fuckers[ply:SteamID64()] = nil
        	    end
        	else
        		tbl_insert(interaction_table.models, 
        		    {
        		    	models = {model},
        		    	warnings = 1,
        		    }
        		)
            end

            Blue.notify(ply, 1, 4, "Do not place bad props!\nWarnings: " .. max_warnings - interaction_table.warnings .. " left.")
        else
            fuckers[ply:SteamID64()] = {
            	models = {model},
            	warnings = 1,
            }

            Blue.notify(ply, 1, 4, "Do not place bad props!\nThis is the first warning.")
        end

